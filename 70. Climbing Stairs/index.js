var climbStairs = function (n) {
    if (n <= 3) return n;


    const temp = {
        1:1,
        2:2,
        3:3
    }
    for (let i = 3; i <= n; i++) {
        
        temp[i] = temp[i - 1] + temp[i - 2];
    }

    return temp[n]

};
